package com.tutorial;

public class main {
    public static void main(String[] args){
        //integer
        int a=22;
        int b=7;
        float c = (float) a/b;
        System.out.printf("%d / %d = %f \n" ,a,b,c);

        //konversi integer ke long integer
        int aa=22;
        int bb=11;
        int cc=a/b;
        long dd=cc;
        System.out.printf("%d / %d = %d konversi nilai long %d  \n" ,aa,bb,cc,dd);

        //cara konversi misalnya dari int ke float
        //int nilai = (float) nilai;
    }
}
